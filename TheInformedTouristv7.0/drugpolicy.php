                <h3>Drug policy</h3>

                <div id="article-summary">

                    <p>The Netherlands is famous for its tolerant drugs policy. But a lot of people don’t realize that drugs are in fact illegal in the Netherlands. Understanding the Dutch drug policy can spare you a lot of trouble. All drugs are forbidden in the Netherlands. It is illegal to produce, possess, sell, import and export drugs. However, the government designed a drug policy with tolerates smoking cannabis under strict terms and conditions.</p>

                    <br/>

                    <img id="main-article-image" src="images/drugpolicy1.jpg">

                    <br/>

                    <p>The drug policy in the Netherlands aims: to reduce the demand for drugs, the supply of drugs and the risks to drug users, their immediate surroundings and society.
                        <br/><br/> The Dutch recognize that it is impossible to prevent people from using drugs altogether. Coffeeshops are therefore allowed to sell small amounts of soft drugs. This pragmatic approach means that authorities can actually focus on the big criminals who profit from drugs and who supply hard drugs.
                    </p>


                    <hr/>
                    <h4>Coffeeshops</h4>
                    <p>Coffee shops are only allowed to sell soft drugs and not more than five grams of cannabis per person per day. Coffee shops are governed by strict laws that control the amount of permitted soft drugs, and the conditions in which it is sold and used. Coffee shops are not allowed to advertise drugs. Persons under the age of 18 are prohibited from buying drugs and are barred access to coffee shops.</p>
                    
                    <p>Kindly visit this link for a complete information on drug laws. <a href="https://sencanada.ca/content/sen/committee/371/ille/library/dolin1-e.htm">National Drug Policy: Netherlands</a> </p>
                    
                    <hr/>
                    <h4>Drug Potency in the Netherlands</h4>
                    <p>If you buy drugs in the Netherlands, you should be keenly aware that the potency of the drugs can differ a great deal from the strength of drugs in your own country. In other words, you never know how strong your purchased drugs will be. If you are trying drugs for the first time or don’t have much experience, be extra careful. We advise you to take a lower dose than you would take in your home country.</p>

                   <p> If you are staying in Amsterdam for more than a week, you can get your drugs tested anonymously at the Jellinek or GGD Amsterdam drug testing service. Drugs you submit to them are analysed in a laboratory. For some pills such as ecstasy which can be matched to those in our database, there is a 50% chance you will get the results on the same day.</p>
                    
                    <p>Kindly visit this page for information on these testing centres and more. <a href="additionalinfo.php">Query Page</a> </p>
                    
