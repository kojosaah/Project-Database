                <h3>Shopping in Amsterdam</h3>

                <div id="article-summary">

                    <p>In general, Amsterdam shopping opening hours are 1-6pm on Mondays and 9/10am-6pm Tuesday to Friday. Most have late night shopping on Thursday until 9pm, and close earlier on Saturdays, at 5pm. Some supermarkets, like Albert Heijn stay open until 8 or 10 pm. On Sundays, you can shop in the city center, Kalverstraat, Damrak, Leidsestraat, and near the Noorderkerk.</p>
                    <img id="main-article-image" src="images/Shoping.jpg">
                    <p>Since many roads in the center are closed to cars, it makes for pleasant shopping in Amsterdam. You will still find busses, trams, taxis and bicycles though, so watch you step. There are several “Amsterdam shopping corridors” in the city and each one has it's own charms and specialties.</p>
                    <p>First off: Nieuwedijk-Kalverstraat, about 1 km full of shops. This pedestrian area, there is no traffic other than a constant stream of tourists, students, scholars, excursionists and locals. The Nieuwedijk is close to Central Station and basically dedicated to clothing, sportswear, music and gift shops with that special Amsterdam shopping vibe. The road leads to Dam Square. Around this square you will find the Royal Palace, the Nieuwe Kerk and Madame Tussaud's.<br> On the other side of the square Amsterdam shopping continues: you are now entering the Kalverstraat. More shoes, clothing, bags, gift shops, cards, perfume, and the occasional coffee shop, fast food restaurant and ice cream salon. You will find that nearly all mainstream brand names have one or more store on this road. Here you can also find Waterstones and nearby at Spui 12 the American Book Center. For a quiet drink away from all these shops you can make a sidestep onto Spui, where you will find many cafes, bars and restaurants and several fast-food chains.<br> Parallel to the Nieuwedijk-Kalverstraat are the Damrak and Rokin. This is a normal road, so beware of bicycles! The Damrak is filled with restaurants, hotels and exchange offices. <br> At the end of the Kalverstraat on the Muntplein, you will find all sorts of tulips, narcissus and other bulbs and flowers on the Flowermarket. The bulbs are ready for export, so you can enjoy them at home too.<br> If you cross the Muntplein and walk through the Regulier Breestraat, which consists of many restaurants, fast-food chains and gift shops, you’ll end up on the Rembrandtplein.<br> If you like a little more luxury, or actually a lot more the Pieter Cornelisz Hooftstraat, affectionately called P.C. Hooftstraat is where you can do some serious damage to your credit card. In this road in the Oud Zuid district of the city, near the Rijksmuseum and the Stedelijk museum, you will find the haute couture boutiques: Cartier, Gucci, Edgar Vos, Tommy Hillfinger and the very impressive Oger shop are all here. If all this is too much you can always go for a walk in the Vondelpark which is situated directly at the end of this road.</p>
