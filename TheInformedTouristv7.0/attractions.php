                <h3>Attractions</h3>
				
				<br/>
				
				<img id="main-article-image" src="images/attractionsmain.jpg">
				
				<br/>
				
                <div id="article-summary">

                    <p>While most visitors to the Netherlands (often referred to as Holland) focus on Amsterdam, the country's vibrant capital with its great museums and art galleries, there are many charming towns and villages in this small yet fascinating country to explore. And because the country is so flat, it's extremely easy to do at least a little sightseeing the Dutch way: by bicycle. Many communities actively encourage the use of pedal-power and provide bikes to explore the sights at no cost. However you choose to see the Netherlands, you're guaranteed a great time in one of the friendliest and most liberal cultures in Europe. The question is which attractions should you spend your time at?</p>
                    <!-- Text taken from http://www.planetware.com/tourist-attractions/netherlands-nl.htm -->
							
					<p>To make it easier for you we have listed the top 10 attractions in the Netherlands according to <a href="http://www.planetware.com/tourist-attractions/netherlands-nl.htm">Planetware</a>:</p>
					
                    <hr/>
                                                            
                    <h4>1. Exploring Amsterdam's Canals </h4>
					
					<img id="main-article-image" src="images/attractions1.jpg">
                    
                    <p>Like Venice, that other famous city built on water, the one enduring memory any visitor to Amsterdam will have is of time spent exploring the city's wonderful canals. While many of Amsterdam's best tourist attractions can be easily accessed by boat tour or water taxi - including most of the major museums and art galleries - there's much to be gained by simply strolling along the smaller, quieter streets that line the waterways. One such neighborhood is the Grachtengordel with its many small bridges and quaint 17th-century homes. You'll be rewarded as you explore these 400-year-old streets by countless examples of beautiful architecture, small boutique shops, cafes, and hotels, as well as many quaint colorful gardens.</p>

                    <hr/>
						
                    <h4>2. Keukenhof: The Garden of Europe</h4>
					
					<img id="main-article-image" src="images/attractions2.jpg">
                    
                    <p>Think of the Netherlands, and you'll inevitably think of tulips, the country's most popular flower. And there's nowhere better to enjoy its rich floral bounty than at the Keukenhof, otherwise known as the Garden of Europe. On the outskirts of Lisse, in what's widely considered the "bulb belt" of the Netherlands, Keukenhof is the largest public garden in the world encompassing more than 70 acres of what was once the former kitchen (or "keuken") garden of a large country estate. Along with its excellent restaurants, sunny patios, and exhibitions - not to mention its more than 700 varieties of tulips - the site is home to the world's largest open-air flower show. The charm of Keukenhof lies in its endless variety of color which, thanks to its massive commercial hot houses, is almost a year-round occurrence. In them, you'll see endless rows of flowering tulips, along with thousands of hyacinths, crocuses, and daffodils.</p>
					
					<hr/>
                    
                    <h4>3. The Rijksmuseum, Amsterdam: A Dutch Treasure Trove</h4>
                    
					<img id="main-article-image" src="images/attractions3.jpg">
					
                    <p>The spectacular Rijksmuseum (aka the Dutch National Museum) in Amsterdam has been collecting rare art and antiquities since 1809. Not surprisingly, its extensive collection today amounts to nearly seven million works of art, including 5,000 paintings in more than 250 rooms, as well as a vast library with some 35,000 books. Apart from its unique collection of old masters, it offers an exhaustive account of the development of art and culture in the Netherlands and is especially rich in traditional Dutch handicrafts, medieval sculpture, and modern art. Be prepared to spend the best part of a day - or longer - exploring this museum's endless treasures.</p>
															
					<hr/>
					
					<h4>4. Hoge Veluwe National Park</h4>
					
					<img id="main-article-image" src="images/attractions4.jpg">
                    
                    <p>You may be surprised to learn that the Netherlands, a relatively small country, boasts one of the world's most diverse national park programs. The largest is Hoge Veluwe National Park between Arnhem and Apeldoorn. Covering nearly 13,800 acres, this national park is the largest continuous nature reserve in the country, as well as being one of the most popular day trip destinations for locals and visitors alike. Featuring dense woodlands in the north, as well as a fascinating sculpture park, the area was once a country estate and hunting reserve, and to this day is home to many red and roe deer. The best-preserved part of the park encompasses an area of dramatic dunes interspersed with heath and woodland and interrupted in the south and east by moraines up to 330 feet high. It's also a popular area for birdwatching, as well as hiking and biking (visitors can access bikes for free).</p>
															
					<hr/>
					
					<h4>5. Anne Frank Museum, Amsterdam</h4>
					
					<img id="main-article-image" src="images/attractions5.jpg">
                    
                    <p>The Anne Frank Museum is a must-see when in Amsterdam. On Prinsengracht, in the home where Anne's family hid for much of WWII (they were Jewish refugees from Frankfurt), is where this remarkable girl wrote her famous diary. Although she died just two months before the war ended, her legacy lives on through her words, which have since been translated into 51 languages. The back of the fully-restored house where the Frank family had their hiding place has been kept in its original state as much as possible and is a poignant monument to a tragic slice of world history and a brave little girl who continues to inspire people around the globe.</p>
														
					<hr/>
					
					<h4>6. The Windmills of Kinderdijk</h4>
					
					<img id="main-article-image" src="images/attractions6.jpg">
                    
                    <p>On the River Noord between Rotterdam and Dordrecht is the famous village of Kinderdijk ("Children's Dike"), which takes its name from an incident during the St. Elizabeth's Day flood of 1421 after a child's cradle had been stranded on the dike. The big draw these days are the fantastically preserved 18th-century windmills. Now UNESCO World Heritage Sites, the 19 Kinderdijk windmills, built between 1722 and 1761, are the largest surviving concentration of windmills in the Netherlands. Originally used to drain the fenlands, these majestic buildings with their impressive 92-foot sails are open to the public from April to October, including special Mill Days when the sails are set in motion.</p>
															
					<hr/>
					
					<h4>7. Van Gogh Museum, Amsterdam</h4>
					
					<img id="main-article-image" src="images/attractions7.jpg">		
                    
                    <p>As befits one of the world's greatest artists, the spectacular Van Gogh Museum in Amsterdam is ranked an impressive 35th in the top art museums globally, attracting almost 1.5 million visitors each year. Home to the world's largest collection of Van Gogh paintings -many donated by the artist's family - this impressive gallery and museum was specially built to showcase the more than 200 paintings, 500 drawings, and 700 letters in its vast collection. Works by his contemporaries are also on display. If you're only able to visit one of the many excellent art museums in the Netherlands, make it this one.</p>
														
					<hr/>
					
					<h4>8. Historic Valkenburg</h4>
					
					<img id="main-article-image" src="images/attractions8.jpg">
                    
                    <p>For those looking for a little ancient history, the Netherlands is not without its own medieval (and earlier) attractions. Romantic little Valkenburg, in the picturesque Geul Valley, boasts the country's only hilltop castle. Long a popular holiday resort, the town's other big draws are its many caves and the spa facilities at Thermae 2000, one of the largest such establishments in the Netherlands. In addition to the ruins of the 12th-century castle on Dwingelrots (Castle Rock), there's also the interesting 14th-century St. Nicolaaskerk Basilica. Another highlight is the town's famous Christmas Market (mid-November to December 23rd) held in the Velvet Caves, the maze of old passageways leading to and from the castle.</p>
													
					<hr/>
					
					<h4>9. Zeeland's Spectacular Dikes</h4>
                    
					<img id="main-article-image" src="images/attractions9.jpg">
					
                    <p>Incorporating the deltas of the Rhine, the Maas, and the Schelde Rivers, Zeeland includes the numerous islands and peninsulas of the southwestern section of the Netherlands. Consisting of some of the world's most recent land formations, much of the area is below sea level and therefore reliant upon impressive dikes as well as modern flood prevention techniques. As you travel the area, you'll see evidence of the engineering project known as the Delta Works. These massive structures - basically hi-tech dams - can control how much water enters the area's key estuaries from the North Sea. Consisting of dams, sluices, locks, dikes, and storm surge barriers, this awe-inspiring project has been declared one of the Seven Wonders of the Modern World.</p>
														
					<hr/>
					
					<h4>10. Kasteel De Haar</h4>
					
					<img id="main-article-image" src="images/attractions10.jpg">
                    
                    <p>Near the lovely old city of Utrecht, the fourth largest in the Netherlands, Kasteel De Haar makes for an excellent day trip. The largest fortification in the country, this spectacular castle - built by the famous Dutch architect, PJH Cuypers - required so much land (it sits on a spectacular 250-acre park) that the entire village of Haarzuilens had to be relocated to accommodate it. While the original castle site was established in the 14th century, this newer structure dates from 1892 and is well worth taking the time to explore. Inside, you'll be rewarded by impressive collections of antiques, furniture, paintings, and tapestries, but it's the gardens that really draw the crowds - along with the castle's fairytale looks.</p>
                    
